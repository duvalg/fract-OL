#include "../includes/fractol.h"

void		init_pos(t_dna *dna)
{
	if (dna->fractale == 1)
	{
		dna->x1 = -2.9999;
		dna->y1 = -2.9999;
		dna->x2 = 2;
		dna->y2 = 2;
	}
	else
	{
		dna->x1 = -3;
		dna->y1 = -2.9;
		dna->x2 = 2.2;
		dna->y2 = 1.6;
	}
}
